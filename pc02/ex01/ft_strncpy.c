#include <unistd.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (i < n && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

int	main(void)
{
	char	dest[20];
	char	src[] = "copiar hasta n";
	unsigned int	n;

	n = 15;
	ft_strncpy(dest, src, n);
	write(1, dest, n);
	return (0);
}
